public class InchToCentimeter {
    public static void main(String[] args) {
        int i = 1;
        double cm;
        System.out.println("Inches      Centimeters");

        while(i <= 10){

            cm = i * 2.54;

            System.out.println(i + "            " + cm);
            i++;
        }


    }

}
